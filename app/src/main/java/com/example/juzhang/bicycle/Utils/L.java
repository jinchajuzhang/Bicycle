package com.example.juzhang.bicycle.Utils;

import android.util.Log;

/**
 * 功能：调试输出
 * 作者：JuZhang
 * 时间：2017/5/13
 */

public class L {
    public static void v(String str){
        Log.v("调试输出",str);
    }
    public static void d(String str){
        Log.d("调试输出",str);
    }
    public static void e(String str){
        Log.e("调试输出",str);
    }
    public static void i(String str){
        Log.i("调试输出",str);
    }

}
